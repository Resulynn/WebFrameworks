    public function store(Request $request)
    {
       $this->validate($request,[
           'Title'=>'required',
           'Body'=>'required'
       ]);

    //append a record

    $post=new Post;
    $post->title=$request->input('Title');
    $post->body=$request->input('Body');
    $post->save();

    return redirect('/post')->with('Success', 'Post Created');

